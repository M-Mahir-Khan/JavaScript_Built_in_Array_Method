// toString method 

// toString() ek JavaScript array method hai jo array ke elements ko ek string mein convert karta hai aur comma separated values ke roop mein unhe represent karta hai.

// Yeh method ek array ke har element ko string mein convert karta hai aur unhe comma se separate kar deta hai. Is method ka output ek single string hota hai, jisme array ke elements ek saath hote hain.

const fruits = ["Apple", "Banana","Orange"]

const result = fruits.toString()

console.log(result); // Output: 'Apple,Banana,Orange'

// pop Method 

//pop() ek JavaScript array method hai jo array se last element ko remove karta hai aur usse return karta hai. Yeh method array ke end se last element ko hata deta hai aur array ke length ko ek kam kar deta hai.

const fruits = ["Apple","Mango","Orange","Banana"]

const removeFruits = fruits.pop()

console.log(fruits);  // Output : ["Apple","Mango","Orange"]
console.log(removeFruits);  // output : "Banana"
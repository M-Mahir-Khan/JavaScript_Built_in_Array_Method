// push method

// push() ek JavaScript array method hai jo ek ya multiple elements ko array ke end mein add karta hai aur array ke naye length ko return karta hai. Is method se aap ek ya multiple values ko array ke end mein insert kar sakte hain.

const fruits = ["Apple","Mango"];

fruits.push("Orange","Banana");
console.log(fruits); // Output : ["Apple","Mango","Orange","Banana"]
// unshift method

//unshift() ek JavaScript array method hai jo ek ya multiple elements ko array ke start mein add karta hai aur array ke naye length ko return karta hai. Is method se aap ek ya multiple values ko array ke shuruwat mein insert kar sakte hain.

const fruits = ["Banana","Orange"];

fruits.unshift("Apple","Mango")
console.log(fruits); //  output : ["Apple","Mango","Banana",'Orange']